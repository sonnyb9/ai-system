Write-Host "========================================" -ForegroundColor Cyan
Write-Host " Fix Python PATH on Windows" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
Write-Host ""

# ====================================================================================================
# ====================================================================================================
# ====================================================================================================
# ====================================================================================================
# ====================================================================================================

$pythonPath = "C:\Python314"
$pythonAppsPath = "C:\Users\asonn\AppData\Local\Microsoft\WindowsApps"

Write-Host "Step 1: Checking Python paths..." -ForegroundColor Yellow
Write-Host "   Checking: $pythonPath" -ForegroundColor Gray
Write-Host "   Checking: $pythonAppsPath" -ForegroundColor Gray

if (Test-Path $pythonPath) {
    Write-Host "✅ Found Python at: $pythonPath" -ForegroundColor Green
} else {
    Write-Host "⚠️  Python not found at: $pythonPath" -ForegroundColor Red
}

if (Test-Path $pythonAppsPath) {
    Write-Host "✅ Found Apps at: $pythonAppsPath" -ForegroundColor Green
} else {
    Write-Host "⚠️  Apps path not found" -ForegroundColor Red
}

Write-Host ""

# ====================================================================================================

Write-Host "Step 2: Adding to PATH..." -ForegroundColor Yellow

$currentPath = [System.Environment]::GetEnvironmentVariable("Path", "User")
Write-Host "Current PATH segments: $($currentPath -split ';' | Select-Object -First 5)" -ForegroundColor Gray

$pathsToAdd = @()
if (-not ($currentPath -like "*$pythonPath*")) {
    Write-Host "   + Adding: $pythonPath" -ForegroundColor Cyan
    $pathsToAdd += $pythonPath
} else {
    Write-Host "   ✓ Already in PATH: $pythonPath" -ForegroundColor Green
}

if (-not ($currentPath -like "*$pythonAppsPath*")) {
    Write-Host "   + Adding: $pythonAppsPath" -ForegroundColor Cyan
    $pathsToAdd += $pythonAppsPath
} else {
    Write-Host "   ✓ Already in PATH: $pythonAppsPath" -ForegroundColor Green
}

Write-Host ""

if ($pathsToAdd.Count -gt 0) {
    $newPath = $currentPath + ";" + ($pathsToAdd -join ";")
    [System.Environment]::SetEnvironmentVariable("Path", $newPath, "User")
    Write-Host "✅ Added to user PATH!" -ForegroundColor Green
} else {
    Write-Host "✅ All Python paths already in PATH!" -ForegroundColor Green
}

Write-Host ""

# ====================================================================================================

Write-Host "Step 3: Adding to session PATH (for current terminal)..." -ForegroundColor Yellow
$env:Path += ";" + $pythonPath
$env:Path += ";" + $pythonAppsPath
Write-Host "✅ Current session PATH updated!" -ForegroundColor Green

Write-Host ""

# ====================================================================================================

Write-Host "Step 4: Testing Python..." -ForegroundColor Yellow
try {
    $result = python --version 2>&1
    Write-Host "✅ $result" -ForegroundColor Green
    Write-Host ""
    Write-Host "✅ Python is now accessible in this session!" -ForegroundColor Green
} catch {
    Write-Host "⚠️  Python still not working in session" -ForegroundColor Yellow
    Write-Host "Error: $_" -ForegroundColor Red
}

Write-Host ""

# ====================================================================================================

Write-Host "Step 5: Instructions..." -ForegroundColor Yellow
Write-Host "   1. Open a NEW PowerShell or Command Prompt terminal" -ForegroundColor Green
Write-Host "   2. Run: python --version" -ForegroundColor Green
Write-Host ""
Write-Host "   Or try:" -ForegroundColor Green
Write-Host "   \$env:Path`r`n" -ForegroundColor Green
Write-Host ""
Write-Host "   Restart terminal and run:" -ForegroundColor Green
Write-Host "   python --version" -ForegroundColor Green

Write-Host ""
Write-Host "========================================" -ForegroundColor Cyan
Write-Host "  Python PATH Fix Complete!" -ForegroundColor Cyan
Write-Host "========================================" -ForegroundColor Cyan
